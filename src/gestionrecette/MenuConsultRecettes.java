    private void btnPrecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrecActionPerformed
        if(this.position>0) {
            afficherRecette(GestionRecette.lesRecettes.get(position-1));
            this.position-=1;
            if(this.position==0) {
                this.btnPrec.setEnabled(false);
            }
            this.btnSuiv.setEnabled(true);
        }
    }//GEN-LAST:event_btnPrecActionPerformed

    private void btnSuivActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuivActionPerformed
        if(this.position+1<GestionRecette.lesRecettes.size()) {
            afficherRecette(GestionRecette.lesRecettes.get(position+1));
            this.position+=1;
            this.btnPrec.setEnabled(true);
            if(this.position>=GestionRecette.lesRecettes.size()) {
                this.btnSuiv.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btnSuivActionPerformed

