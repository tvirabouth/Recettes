    private void afficherRecette(Recette laRecette)
    {
        this.inputID.setText(Integer.toString(laRecette.getIdRecette()));
        this.inputNom.setText(laRecette.getNomRecette());
        this.inputTpsPrep.setText(laRecette.getTempsPreparation());
        this.inputTpsCuiss.setText(laRecette.getTempsCuisson());
        this.inputNiveauDifficulte.setText(Integer.toString(laRecette.getNiveauDifficulte()));
        this.inputType.setText(Integer.toString(laRecette.getIdType()));
    }

